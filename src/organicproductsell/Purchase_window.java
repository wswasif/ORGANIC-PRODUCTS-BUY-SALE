 private void populateJTableOnFormLoad(){
        // TODO add your handling code here:
 
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0); //start inserting from first row
        tableModel.setColumnCount(3); //set column number
        
        //setting column headers
        jTable1.getColumnModel().getColumn(0).setHeaderValue("ID");
        jTable1.getColumnModel().getColumn(1).setHeaderValue("Name");
        jTable1.getColumnModel().getColumn(2).setHeaderValue("Purchase via");
        jTable1.getTableHeader().repaint();
        
        try {
            DBConnect dbc = new DBConnect();
            ResultSet rs = dbc.getResultSet();
            Show_Purchase_Info_In_Jtable();
               
            while (rs.next()) {
                String id = rs.getString("id");
                String name = rs.getString("name");
                String purchase = rs.getString("purchase via");
                
                System.out.println("\nStudent Id: " + id + "\n"
                        + "Name: " + name + "\n"
                        + "Purchase via " + purchase);
                       
                tableModel.addRow(new Object[]{id, name, purchase});
            }
        } catch (Exception e) {
            System.out.println("Error: " + e);
            e.printStackTrace();
            
        }
    }
