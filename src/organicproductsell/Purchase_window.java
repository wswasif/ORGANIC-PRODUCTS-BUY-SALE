    public Connection getConnection(){
        Connection con= null;
        try{
            con= DriverManager.getConnection("jdbc:mysql://localhost/purchase_info","root","");
            JOptionPane.showMessageDialog(null,"Connected");
            return con;
        }catch(SQLException ex){
            Logger.getLogger(Main_window.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null,"Not connected");
            return null;
        }
        
    }
    
    public ArrayList<Purchase> getPurchaseinfo(){
        ArrayList<Purchase> purchaseInfo= new ArrayList<Purchase>();
            Connection con= getConnection();
            String query = "SELECT * FROM purchase";
            
            Statement st;
            ResultSet rs;
        try {
          
            st=con.createStatement();
            rs=st.executeQuery(query);
            Purchase purchase;
            
            while(rs.next()){
                purchase= new Purchase(rs.getInt("ID"),rs.getString("Name"),rs.getString("Purchase"));
                purchaseInfo.add(purchase);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Purchase_window.class.getName()).log(Level.SEVERE, null, ex);
        }
        return purchaseInfo;
    }
    
    public void Show_Purchase_Info_In_Jtable(){
        ArrayList<Purchase> list = getPurchaseinfo();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        Object[] row = new Object[3];
        for(int i =0; i < list.size(); i++){
        row[0]=list.get(i).getId();
        row[1]=list.get(i).getName();
        row[2]=list.get(i).getPurchase();
        
        model.addRow(row);
    }
    }

